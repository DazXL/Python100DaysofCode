#Task 1 is about DATA TYPES

#subscripting, pulling a particular element from a string.
print("Hello"[0]) #prints the character in the string at the position "0"

#Strings Data Type
stringData = "Name"
print(stringData)
print(type(stringData))
print("Your " + stringData) #Concatenation

#Integers Data Type

integerData = 123
print(integerData)
print(type(integerData))
print(400 + integerData) #Calculation

#Floats Data Type

floatData = 1.23
print(floatData)
print(type(floatData))
print(floatData + integerData) #calculates with decimals

#Booleans Data Type
booleanData = True
print(True)
print(False)
print(type(booleanData))